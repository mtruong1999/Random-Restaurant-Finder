<h1>About</h1>
<p class="flow-text">One common problem that exists when eating out is indecisiveness
when choosing a restaurant. This app was created to provide a fun solution to this problem
by providing you a random option. Go with the first option given to you to be spontaneuous or,
if you're REALLY indecisive, keep generating random restaurants until you see what you've
really wanted to eat all along but were too afraid to admit it.</p>
